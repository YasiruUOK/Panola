package panola.model;

/**
 *
 * @author Yasiru
 */
public class StudentAddmissionPayment {
    private String NIC;
    private String PID;
    private double fee;

    public StudentAddmissionPayment() {
    }

    public StudentAddmissionPayment(String NIC, String PID, double fee) {
        this.NIC = NIC;
        this.PID = PID;
        this.fee = fee;
    }

    /**
     * @return the NIC
     */
    public String getNIC() {
        return NIC;
    }

    /**
     * @param NIC the NIC to set
     */
    public void setNIC(String NIC) {
        this.NIC = NIC;
    }

    /**
     * @return the PID
     */
    public String getPID() {
        return PID;
    }

    /**
     * @param PID the PID to set
     */
    public void setPID(String PID) {
        this.PID = PID;
    }

    /**
     * @return the fee
     */
    public double getFee() {
        return fee;
    }

    /**
     * @param fee the fee to set
     */
    public void setFee(double fee) {
        this.fee = fee;
    }
    
}
