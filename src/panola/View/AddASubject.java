    private void feeTextKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_feeTextKeyReleased
        String text = feeText.getText();
        boolean matches = text.matches("[0-9]{0,4}");
        if (!matches) {
            feeText.setText(text.substring(0, text.length() - 1));
        }
    }//GEN-LAST:event_feeTextKeyReleased

