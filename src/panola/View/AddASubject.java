    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        String id=subjectIDText.getText();
        String name=subjectNametext.getText();
        double fee=Double.parseDouble(feeText.getText());
        String stream=steamCombo.getSelectedItem().toString();
        try {
            String TeacherID=TeacherController.getID(teacherNameCombo.getSelectedItem().toString());
            Subject subject=new Subject(id,name,fee,stream,TeacherID);
            int res=SubjectController.addSubject(subject);
            if(res>0){
                JOptionPane.showMessageDialog(this, "Subject Added Sucsessfully ");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddASubject.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddASubject.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_saveButtonActionPerformed

